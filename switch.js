var numero = 1;
switch (numero) {
    case 1:
        console.log("soy uno");
        break;
    case 2:
        console.log("soy dos");
        break;
    case 3:
        console.log("soy un 3");
        break
    case 10:
        console.log("soy un 10");
        break;
    default:
        console.log("no soy nada");
        break;
}